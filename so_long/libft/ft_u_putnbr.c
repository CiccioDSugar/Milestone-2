#include "libft.h"

int	ft_u_putnbr(unsigned int nb)
{
	int	i;

	i = 0;
	if (nb >= 10)
	{
		i += ft_u_putnbr(nb / 10);
	}
	nb = (nb % 10) + 48;
	i += write(1, &nb, 1);
	return (i);
}
