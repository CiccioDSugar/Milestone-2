#include "libft.h"

int	ft_putnbr(int nb)
{
	int	i;

	i = 0;
	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		write (1, "-", 1);
		nb *= -1;
		i = 1;
	}
	if (nb <= 2147483647)
	{
		if (nb >= 10)
		{
			i += ft_putnbr(nb / 10);
		}
		nb = (nb % 10) + 48;
		i += write(1, &nb, 1);
	}
	return (i);
}
